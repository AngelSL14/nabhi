package us.gonet.serverutils.model.jke;

public class TmkEntity {

    private String clazz;
    private String error;
    private String encrypType;
    private String mode;
    private String tpk;
    private String sequence;
    private String pinBlock;

    public String getClazz() {
        return clazz;
    }

    public void setClazz(String clazz) {
        this.clazz = clazz;
    }

    public String getError() {
        return error;
    }

    public void setError(String error) {
        this.error = error;
    }

    public String getEncrypType() {
        return encrypType;
    }

    public void setEncrypType(String encrypType) {
        this.encrypType = encrypType;
    }

    public String getMode() {
        return mode;
    }

    public void setMode(String mode) {
        this.mode = mode;
    }

    public String getTpk() {
        return tpk;
    }

    public void setTpk(String tpk) {
        this.tpk = tpk;
    }

    public String getSequence() {
        return sequence;
    }

    public void setSequence(String sequence) {
        this.sequence = sequence;
    }

    public String getPinBlock() {
        return pinBlock;
    }

    public void setPinBlock(String pinBlock) {
        this.pinBlock = pinBlock;
    }
}
